#include "get_next_line.h"

size_t	ft_strlen(const char *s)
{
	size_t	len = 0;
	while (s && s[len])
		len++;
	return (len);
}

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}

char	*ft_strdup(const char *s)
{
	size_t	len = ft_strlen(s);
	char	*copy = (char *)malloc(len + 1);
	size_t	i = 0;

	if (!copy)
		return (NULL);
	while (i < len)
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1 = s1 ? ft_strlen(s1) : 0;
	size_t	len2 = s2 ? ft_strlen(s2) : 0;
	char	*joined = (char *)malloc(len1 + len2 + 1);
	size_t	i = 0, j = 0;

	if (!joined)
		return (NULL);
	while (s1 && s1[i])
	{
		joined[i] = s1[i];
		i++;
	}
	while (s2 && s2[j])
		joined[i++] = s2[j++];
	joined[i] = '\0';
	return (joined);
}


char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i = 0;
	size_t	s_len = ft_strlen(s);
	char	*sub;

	if (!s || start >= s_len)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	sub = (char *)malloc(len + 1);
	if (!sub)
		return (NULL);
	while (i < len && s[start + i])
	{
		sub[i] = s[start + i];
		i++;
	}
	sub[i] = '\0';
	return (sub);
}



